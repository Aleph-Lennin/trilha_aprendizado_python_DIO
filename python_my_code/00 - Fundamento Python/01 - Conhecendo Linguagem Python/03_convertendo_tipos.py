# convertendo um valor string para inteiro
valor = "500"
valor = int(valor)
print(valor)
print(type(valor)) # mostra a class do objeto

# convertendo um valor inteiro para float
valor = float(valor)
print(valor)
print(type(valor))
# print("seu saldo " + valor) Erro, não é possivel concatenar um tipo float e inteiro com string

# convertendo um valor float para string
valor = str(valor)
print(valor)
print(type(valor))
print("seu saldo " + valor) # Aqui da certo, pois convertemos para tipo str



